    private void tampil_menu(){
        Object[]baris = {"Kode Menu","Nama Menu","Harga"};
        tabmode= new DefaultTableModel(null,baris);
        table_menu.setModel(tabmode);
        String sql = "select * from tabel_menu order by kode_menu asc";
        try {
            Connection konek = new koneksi_mysql().getConnection();
            Statement stat = konek.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String kode_menu = hasil.getString("kode_menu");
                String nama_menu = hasil.getString("nama_menu");
                String harga = hasil.getString("harga");
                String[]data = {kode_menu, nama_menu, harga};
                tabmode.addRow(data);
                }
            } catch (Exception e){
            JOptionPane.showMessageDialog(null,"menampilkan data GAGAL","informasi",JOptionPane.INFORMATION_MESSAGE);
        }
    }

